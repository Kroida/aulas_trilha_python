# Variáveis e constantes são elementos de algoritmo para manipular dados
# Variáveis são espaços de memória que podem ser alterados
# Constantes são espaços de memória que não podem ser alterados

PI = 3.141519 # Constantes são definidas com letras maiúsculas
raio = 5 # Variável

area = PI * raio * raio

print(area)